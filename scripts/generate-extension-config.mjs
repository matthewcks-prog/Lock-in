import { writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const rawEnv = (process.env.LOCKIN_APP_ENV || process.env.SUPABASE_ENV || 'local').toLowerCase();

const envKey = rawEnv === 'production' || rawEnv === 'prod'
  ? 'prod'
  : rawEnv === 'development' || rawEnv === 'dev'
    ? 'dev'
    : 'local';

const envLabel = envKey === 'prod' ? 'production' : envKey === 'dev' ? 'development' : 'local';
const envUpper = envKey.toUpperCase();

const defaultBackendUrl = envKey === 'prod'
  ? 'https://lock-in-backend-prod.mangoriver-ea919cda.australiaeast.azurecontainerapps.io'
  : envKey === 'dev'
    ? 'https://lock-in-backend.mangoriver-ea919cda.australiaeast.azurecontainerapps.io'
    : 'http://localhost:3000';

const backendUrl =
  process.env[`BACKEND_URL_${envUpper}`] ||
  process.env.BACKEND_URL ||
  defaultBackendUrl;

const defaultSupabaseUrl = envKey === 'local' ? 'http://127.0.0.1:54331' : '';
const supabaseUrl =
  process.env[`SUPABASE_URL_${envUpper}`] ||
  process.env.SUPABASE_URL ||
  defaultSupabaseUrl;

const supabaseAnonKey =
  process.env[`SUPABASE_ANON_KEY_${envUpper}`] ||
  process.env.SUPABASE_ANON_KEY ||
  '';

const configSource = `/**
 * Extension Runtime Configuration
 *
 * Generated by scripts/generate-extension-config.mjs
 * Do not edit manually.
 */
(function () {
  const root = typeof window !== 'undefined' ? window : self;
  if (root.LOCKIN_CONFIG) {
    return;
  }

  const APP_ENV = ${JSON.stringify(envLabel)};
  const IS_PRODUCTION = APP_ENV === 'production';

  root.LOCKIN_CONFIG = {
    APP_ENV,
    IS_PRODUCTION,
    BACKEND_URL: ${JSON.stringify(backendUrl)},
    SUPABASE_URL: ${JSON.stringify(supabaseUrl)},
    SUPABASE_ANON_KEY: ${JSON.stringify(supabaseAnonKey)},
    SUPABASE_ENVIRONMENT: ${JSON.stringify(envLabel)},
    SESSION_STORAGE_KEY: 'lockinSupabaseSession',
    TOKEN_EXPIRY_BUFFER_MS: 60000,
    DEBUG_PANOPTO_RESOLVER: !IS_PRODUCTION,
  };

  if (!IS_PRODUCTION && typeof console !== 'undefined') {
    console.log('[Lock-in] Extension config generated:', {
      environment: APP_ENV,
      supabase: root.LOCKIN_CONFIG.SUPABASE_ENVIRONMENT,
      backendUrl: root.LOCKIN_CONFIG.BACKEND_URL,
    });
  }
})();`;

const outputPath = resolve('extension', 'config.js');
writeFileSync(outputPath, configSource, { encoding: 'utf8' });
console.log(`[lock-in] Wrote ${outputPath} for ${envLabel} environment.`);
